App::~App() { ImGui::SFML::Shutdown(m_renderWindow); }

void App::run()
{
    sf::Clock loopClock;
    while (m_renderWindow.isOpen()) {
        // Event loop
        for (sf::Event e; m_renderWindow.pollEvent(e);) {
            ImGui::SFML::ProcessEvent(e);
            if (e.type == sf::Event::Closed)
                m_renderWindow.close();
        }

        // Update stage
        const auto dt = loopClock.restart();
        ImGui::SFML::Update(m_renderWindow, dt);
        updateUI();

        // Rendering logic
        m_renderWindow.clear();
        ImGui::SFML::Render(m_renderWindow);
        m_renderWindow.display();
    }
}

void App::updateUI()
{
    static std::vector<char> pathData;
    if (pathData.empty()) {
        pathData.resize(500);
        memset(pathData.data(), 0, pathData.size());
    }

    // Everything we do will be added to the main window, to achieve this all GUI code we add should be called between
    // ImGui::Begin("main-window") and its matching ImGui::End()
    ImGui::Begin("main-window",
                 nullptr,
                 ImGuiWindowFlags_NoTitleBar | ImGuiWindowFlags_NoResize | ImGuiWindowFlags_NoMove
                     | ImGuiWindowFlags_NoScrollbar);
    ImGui::SetWindowSize(m_renderWindow.getSize());
    ImGui::SetWindowPos({ 0, 0 });

    ImGui::InputText("Input File Path", pathData.data(), pathData.size());
    if (ImGui::Button("Export")) {
        if (tt::UnpackTextures({ pathData.data() })) {
            m_failedExport = false;
        } else {
            m_failedExport = true;
        }
    }

    static ImGui::FileBrowser fileDialog;
    // Button to open a broswer to find the texture file.
    // This is WIP don't mind the code makes no sense we debug as we go.
    if (ImGui::Button("Browse")) {
        // Header Text.
        fileDialog.SetTitle("Choose File");
        // Sets file types we are restircted to.
        fileDialog.SetTypeFilters({ ".tex" });
        fileDialog.Open();
    }

    fileDialog.Display();
    if (fileDialog.HasSelected()) {
        // Sets the path to the file.
        auto filePath = fileDialog.GetSelected().string();
        fileDialog.ClearSelected();
    }

    // If we failed to export we'll display a text box saying so
    if (m_failedExport) {
        ImGui::TextColored(ImColor(1, 0, 0, 0), "Failed to export, check input file path is valid");
    }
    ImGui::End();
}

